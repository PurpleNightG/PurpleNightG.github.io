-- 创建课程表
CREATE TABLE IF NOT EXISTS courses (
  id INT AUTO_INCREMENT PRIMARY KEY,
  code VARCHAR(10) NOT NULL UNIQUE COMMENT '课程编号，如 1.1, 2.3',
  name VARCHAR(200) NOT NULL COMMENT '课程名称',
  category VARCHAR(50) NOT NULL COMMENT '类别（入门课程、标准技能一阶等）',
  difficulty VARCHAR(20) NOT NULL COMMENT '难度（初级、中级、高级）',
  hours INT NOT NULL COMMENT '课时',
  `order` INT NOT NULL COMMENT '排序序号',
  description TEXT COMMENT '课程描述',
  created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
  INDEX idx_order (`order`),
  INDEX idx_category (category),
  INDEX idx_code (code)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci COMMENT='课程管理表';

